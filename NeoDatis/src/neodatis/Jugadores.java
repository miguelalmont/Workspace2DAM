package neodatis;

public class Jugadores {
	
	String nombre;
	
	String deporte;
	
	String ciudad;
	
	int edad;
	
	public Jugadores() {
		
	}

	public Jugadores(String nombre, String deporte, String ciudad, int edad) {
		this.nombre = nombre;
		this.deporte = deporte;
		this.ciudad = ciudad;
		this.edad = edad;
	}

	
	
}
